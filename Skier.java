
public class Skier {
	
	private double age;
	private String name;
	private String level;
	private String rentalPref;
	
	
	public double getAge() {
		return age;
	}
	public void setAge(double age) {
		this.age = age;
	}
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
	public String getRentalPref() {
		return rentalPref;
	}
	public void setRentalPref(String rentalPref) {
		this.rentalPref = rentalPref;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

}


